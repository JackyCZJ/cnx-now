// 场馆数据：hours 规则按星期几写入，方便后续调整
export interface HoursRule {
  /** 0=周日 1=周一 … 6=周六 */
  days: number[]
  /** "HH:MM" */
  open: string
  /** "HH:MM"，跨午夜用 "24:00" */
  close: string
}

export interface Venue {
  id: string
  name: string
  /** Google Maps 搜索用英文名/地址 */
  mapQuery: string
  category: string
  area: string
  /** 展示用营业时间说明 */
  hoursText: string
  rules: HoursRule[]
  /** 从古城区出发的大致交通时间 */
  transport: string
  note?: string
  /** 展示用电话 */
  phone?: string
  /** tel: 链接用 */
  phoneTel?: string
}

